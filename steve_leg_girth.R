right_girth <- c(13.25, 14.25, 13.5, 13.5)
df <- data.frame(date,left_girth, right_girth)
plot(df, type = "l")
library(ggplot2)
ggplot() + geom_line(data=df, mapping = aes(x = date, y = left_girth))
